#ifndef ARANGOD_CLUSTER_SERVER_JOB_H
#define ARANGOD_CLUSTER_SERVER_JOB_H 1

#include "Basics/Common.h"
#include "Basics/Exceptions.h"
#include "Basics/Mutex.h"
#include "Dispatcher/Job.h"
#include "HttpServer/HttpHandler.h"

struct TRI_server_t;


namespace triagens {
namespace arango {
class HeartbeatThread;
class ApplicationV8;

////////////////////////////////////////////////////////////////////////////////
/// @brief general server job
////////////////////////////////////////////////////////////////////////////////

class ServerJob : public triagens::rest::Job {
 private:
  ServerJob(ServerJob const&) = delete;
  ServerJob& operator=(ServerJob const&) = delete;

  
 public:
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief constructs a new db server job
  ////////////////////////////////////////////////////////////////////////////////

  ServerJob(HeartbeatThread* heartbeat, TRI_server_t* server,
            ApplicationV8* applicationV8);

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief destructs a db server job
  ////////////////////////////////////////////////////////////////////////////////

  ~ServerJob();

  
 public:
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief abandon job
  ////////////////////////////////////////////////////////////////////////////////

  void abandon();

  
 public:
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief whether or not the job is detached
  ////////////////////////////////////////////////////////////////////////////////

  inline bool isDetached() const { return true; }

  ////////////////////////////////////////////////////////////////////////////////
  /// {@inheritDoc}
  ////////////////////////////////////////////////////////////////////////////////

  void work() override;

  ////////////////////////////////////////////////////////////////////////////////
  /// {@inheritDoc}
  ////////////////////////////////////////////////////////////////////////////////

  bool cancel() override;

  ////////////////////////////////////////////////////////////////////////////////
  /// {@inheritDoc}
  ////////////////////////////////////////////////////////////////////////////////

  void cleanup(rest::DispatcherQueue* queue) override;

  ////////////////////////////////////////////////////////////////////////////////
  /// {@inheritDoc}
  ////////////////////////////////////////////////////////////////////////////////

  bool beginShutdown() {
    _shutdown = 1;
    return true;
  }

  ////////////////////////////////////////////////////////////////////////////////
  /// {@inheritDoc}
  ////////////////////////////////////////////////////////////////////////////////

  void handleError(basics::Exception const& ex) override {}

  
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief execute job
  ////////////////////////////////////////////////////////////////////////////////

  bool execute();

  
 private:
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief the heartbeat thread
  ////////////////////////////////////////////////////////////////////////////////

  HeartbeatThread* _heartbeat;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief server
  ////////////////////////////////////////////////////////////////////////////////

  TRI_server_t* _server;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief v8 dispatcher
  ////////////////////////////////////////////////////////////////////////////////

  ApplicationV8* _applicationV8;

  
 protected:
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief shutdown in progress
  ////////////////////////////////////////////////////////////////////////////////

  volatile sig_atomic_t _shutdown;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief server is dead lock
  ////////////////////////////////////////////////////////////////////////////////

  basics::Mutex _abandonLock;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief server is dead
  ////////////////////////////////////////////////////////////////////////////////

  bool _abandon;
};
}
}

#endif


